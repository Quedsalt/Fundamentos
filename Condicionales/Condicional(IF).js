// Estructuras Condicionales o Estructuras de Control en JavaScript

// Definimos una variable 'nombre' con el valor "Nico"
let nombre = "Nico"

// Iniciamos una estructura condicional 'if' para verificar el valor de 'nombre'
if (nombre === "Diego") {
    // Si 'nombre' es igual a "Diego", se ejecuta este bloque
    console.log("Hola Diego")
} else if (nombre === "Nico") {
    // Si 'nombre' es igual a "Nico", se ejecuta este bloque
    console.log("Hola Nico")
} else {
    // Si ninguna de las condiciones anteriores se cumple, se ejecuta este bloque
    console.log("Nombre no Encontrado")
}